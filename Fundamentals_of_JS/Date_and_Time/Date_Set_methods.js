const date = new Date();

// Implement set() methods to set date, time, and month, etc. 
date.setDate(10); // DD
console.log(date);
date.setMilliseconds(23353553454343); // MS
console.log(date);
date.setMonth(8) + 1; // MM
console.log(date);
date.setMinutes(23); // MM(minutes)
console.log(date);
date.setHours(12); // HH
console.log(date);

// There are many set() methods in Date Object
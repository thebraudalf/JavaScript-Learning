const date = new Date();

// Implement get() methods to get date, time, and month, etc. 
console.log(date.getDate()); // Current DD
console.log(date.getMilliseconds()); // Current MS
console.log(date.getMonth() + 1); // Current MM
console.log(date.getDay()); // Current D
console.log(date.getHours()); // Current HH

// There are many get() methods in Date Object